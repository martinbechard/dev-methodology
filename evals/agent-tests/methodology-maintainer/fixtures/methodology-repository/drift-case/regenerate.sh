# Regenerates the synthetic drift-case adapter from sample skill metadata.
set -eu
script_dir=$(CDPATH= cd -- "$(dirname -- "$0")" && pwd)
cd "$script_dir"
description=$(sed -n 's/^description: //p' skills/sample/SKILL.md | head -n 1)
output=$(mktemp)
trap 'rm -f "$output"' EXIT
{
  echo '# Generated by regenerate.sh. Do not edit by hand.'
  echo 'name = "sample"'
  printf 'description = "%s"\n' "$description"
} > "$output"
if [ "${1:-}" = "--check" ]; then
  cmp "$output" generated/adapters/sample.toml
else
  cp "$output" generated/adapters/sample.toml
fi
